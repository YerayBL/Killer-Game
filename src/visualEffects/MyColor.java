package visualEffects;

/**
 *
 * @author pau
 */
public class MyColor {

    int a, r, g, b;
    
    public MyColor(int a, int b, int g, int r){
        this.a = a;
        this.b = b;
        this.g = g;
        this.r = r;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getR() {
        return r;
    }

    public void setR(int r) {
        this.r = r;
    }

    public int getG() {
        return g;
    }

    public void setG(int g) {
        this.g = g;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }
    
    
    
}
